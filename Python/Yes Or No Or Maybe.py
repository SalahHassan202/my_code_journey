# Yes Or No Or Maybe.....

typed = input("Please Typed (yes) or (no) or (maybe) \n")
if typed == "yes" :
    print("You Typed yes")
elif typed == "no" :
    print("You Typed no")
elif typed == "maybe" :
    print("You Typed maybe")
else :
    print("You Typed " + typed + " Which Is Not An Opition \nPlease , Stick To The Options ")