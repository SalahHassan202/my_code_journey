// print element of array using for statement (2D ARRAY)
#include <iostream>
#include <bits/stdc++.h>
using namespace std ;
int main ()
{


//print element of array using for statement (2D ARRAY)


int x[2][3]={{41,30,17},{26,11,90}};
// i == row   &&   j == coulmn
for(int i=0 ; i<2 ;i++)
{
    for(int j=0 ; j<3 ;j++)
    {
        cout<<x[i][j]<<endl;
    }
}




return 0;

}








