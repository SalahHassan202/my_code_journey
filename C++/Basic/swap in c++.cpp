//  swap in c++
#include <iostream>
using namespace std;
int main()
{

int x=10 ; 
int y =20 ;
int temp ;

temp =x ;   // temp =10 ;
x =y;       // x =20 ;
y =temp ;   // y = 10 ;

// this is called swap between to number 
 

cout<<"x is = "<<x<<endl;
cout<<"y is = "<<y<<endl;

    return 0 ;
}
